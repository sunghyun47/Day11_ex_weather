package com.google.weather;

public class Weather {

	private String city;
	private String state;
	private int gion;
	private int hum;
	private double mise;
	
	
	public String getCity() {
		return city;
	}
	public void setCity(String city) {
		this.city = city;
	}
	public String getState() {
		return state;
	}
	public void setState(String state) {
		this.state = state;
	}
	public int getGion() {
		return gion;
	}
	public void setGion(int gion) {
		this.gion = gion;
	}
	public int getHum() {
		return hum;
	}
	public void setHum(int hum) {
		this.hum = hum;
	}
	public double getMise() {
		return mise;
	}
	public void setMise(double mise) {
		this.mise = mise;
	}
	
	
}
